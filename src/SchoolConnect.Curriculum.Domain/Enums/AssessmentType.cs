namespace SchoolConnect.Curriculum.Domain.Enums;

/// <summary>
/// Types of assessments used in the curriculum.
/// </summary>
public enum AssessmentType
{
    /// <summary>
    /// Written examination.
    /// </summary>
    WrittenExamination,

    /// <summary>
    /// Oral examination or presentation.
    /// </summary>
    OralExamination,

    /// <summary>
    /// Practical examination or demonstration.
    /// </summary>
    PracticalExamination,

    /// <summary>
    /// Project-based assessment.
    /// </summary>
    Project,

    /// <summary>
    /// Coursework or assignments.
    /// </summary>
    Coursework,

    /// <summary>
    /// Portfolio assessment.
    /// </summary>
    Portfolio,

    /// <summary>
    /// Practical Assessment Task (PAT).
    /// </summary>
    PracticalAssessmentTask,

    /// <summary>
    /// Class tests or quizzes.
    /// </summary>
    Test,

    /// <summary>
    /// Research task or investigation.
    /// </summary>
    Research,

    /// <summary>
    /// Performance assessment (e.g., drama, music).
    /// </summary>
    Performance,

    /// <summary>
    /// Continuous assessment.
    /// </summary>
    ContinuousAssessment
}
