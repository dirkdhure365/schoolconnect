namespace SchoolConnect.Curriculum.Domain.Enums;

/// <summary>
/// Cognitive levels based on Bloom's Taxonomy.
/// </summary>
public enum CognitiveLevel
{
    /// <summary>
    /// Remembering facts and basic concepts.
    /// </summary>
    Knowledge,

    /// <summary>
    /// Understanding meaning and interpretation.
    /// </summary>
    Comprehension,

    /// <summary>
    /// Applying knowledge to new situations.
    /// </summary>
    Application,

    /// <summary>
    /// Breaking information into parts and examining relationships.
    /// </summary>
    Analysis,

    /// <summary>
    /// Creating new ideas and combining information.
    /// </summary>
    Synthesis,

    /// <summary>
    /// Justifying decisions and making judgments.
    /// </summary>
    Evaluation
}
